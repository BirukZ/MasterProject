package manager.employeeManager;

/**
 *
 * @author mekete
 */
public class EmployeeFamilyModel {

    int familyId;
    String employeeId;
    String familyFirstName;
    String familyMiddleName;
    String familyLastName;
    String familyFullName;
    String familyRelationship;
    String familyAliveOrDied;
    String familyIsHeir;
    String familyIsEmergencyContact;
    String familyGender;
    String databaseStatus;

    public EmployeeFamilyModel() {
    }

    public EmployeeFamilyModel(int familyId, String employeeId, String familyFirstName, String familyMiddleName, String familyLastName, String familyRelationship, String familyAliveOrDied, String familyIsHeir, String familyIsEmergencyContact, String familyGender, String databaseStatus) {
        this.familyId = familyId;
        this.employeeId = employeeId;
        this.familyFirstName = familyFirstName;
        this.familyMiddleName = familyMiddleName;
        this.familyLastName = familyLastName;
        this.familyRelationship = familyRelationship;
        this.familyAliveOrDied = familyAliveOrDied;
        this.familyIsHeir = familyIsHeir;
        this.familyIsEmergencyContact = familyIsEmergencyContact;
        this.familyGender = familyGender;
        this.databaseStatus = databaseStatus;
    }

 
    public String getDatabaseStatus() {
        return databaseStatus;
    }

    public void setDatabaseStatus(String databaseStatus) {
        this.databaseStatus = databaseStatus;
    }

    public int getFamilyId() {
        return familyId;
    }

    public void setFamilyId(int familyId) {
        this.familyId = familyId;
    }



    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    public String getFamilyAliveOrDied() {
        return familyAliveOrDied;
    }

    public void setFamilyAliveOrDied(String familyAliveOrDied) {
        this.familyAliveOrDied = familyAliveOrDied;
    }

    public String getFamilyFirstName() {
        return familyFirstName;
    }

    public void setFamilyFirstName(String familyFirstName) {
        this.familyFirstName = familyFirstName;
    }

    public String getFamilyFullName() {
        return familyFullName;
    }

    public void setFamilyFullName(String familyFullName) {
        this.familyFullName = familyFullName;
    }

    public String getFamilyGender() {
        return familyGender;
    }

    public void setFamilyGender(String familyGender) {
        this.familyGender = familyGender;
    }

    public String getFamilyIsEmergencyContact() {
        return familyIsEmergencyContact;
    }

    public void setFamilyIsEmergencyContact(String familyIsEmergencyContact) {
        this.familyIsEmergencyContact = familyIsEmergencyContact;
    }

    public String getFamilyIsHeir() {
        return familyIsHeir;
    }

    public void setFamilyIsHeir(String familyIsHeir) {
        this.familyIsHeir = familyIsHeir;
    }

    public String getFamilyLastName() {
        return familyLastName;
    }

    public void setFamilyLastName(String familyLastName) {
        this.familyLastName = familyLastName;
    }

    public String getFamilyMiddleName() {
        return familyMiddleName;
    }

    public void setFamilyMiddleName(String familyMiddleName) {
        this.familyMiddleName = familyMiddleName;
    }

    public String getFamilyRelationship() {
        return familyRelationship;
    }

    public void setFamilyRelationship(String familyRelationship) {
        this.familyRelationship = familyRelationship;
    }
}
